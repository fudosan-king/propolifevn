				</div><br>
				<span style="font-weight: bold; font-size: 18px;">
					家族帯同ビザについて
				</span><br>
				<div>
					ビジネスビザ、駐在員事務所ビザ、レジデンスカードに緋もづいた家族帯同ビザを取得することができます。<br>
					例えばご主人様のビザに奥様、お子様の帯同ビザの発行が可能です。<br>
					【取得必要書類】<br>
					-ビザを持っている方のパスポート<br>
					-ビザを持っている方のビザ、レジデンスカード写真部<br>
					-ビザを持っている方のお勤め先のライセンスコピー<br>
					-ビザを取得したい方のパスポート<br>
					-戸籍謄本（家族全員分の写し。専門翻訳機関に提出致します）<br>
					-ビザを取得したい方の顔写真　2枚　<br>
					-ベトナム居住証明書 <br>
					上記書類をメールで頂き申請書をお作りします。働かれている方の会社の代表者のサインと会社印を頂き書類と合わせて申請致します。<br>
					申請後約8日平日で公電の取得が可能です。（時期により変動しています）
