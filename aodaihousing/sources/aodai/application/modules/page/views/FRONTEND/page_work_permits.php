					申請後約4日～7日平日で公電の取得が可能です。（時期により変動しています）<br>
					＊2018年8月20日から長期ビザの発給は停まったままとなっています。<br>
					＊現在国内更新は可能ですが、最新詳細状況はお問い合わせください。<br>
					＊現在国内では、観光からビジネスへの変更はできません。<br>
					＊取得期間と費用に関して随時変更が起きておりますので、最新詳細状況はお問い合わせください。
