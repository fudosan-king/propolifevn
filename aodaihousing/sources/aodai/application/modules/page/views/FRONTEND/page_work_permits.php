					ベトナムビザについて
				</span><br>
				<div>
					幣サービスは、経験豊富な日本人担当者とベトナム人担当者で日本語でのサービス提供をさせて頂いております。<br>
					日本人の方がご利用になられることが多い、観光、ビジネス、駐在員事務所、レジデンスカード、家族帯同ビザなどベトナムビザのお取り扱いを行っており、これまで2500案件以上のお手伝いをしてきた実績がございます。<br>
					現在、ベトナムに入国する際は、日本人へのビザの緩和措置として、ビザなしで入 国して15日間まで滞在が可能になっております。<br>
					ただし、連続しての入出国には制限があり、1度入国してから30日以上経過していることがビザなしで入国できる条件となっております。<br>
					それ以外の入国の場合にはビザが必要になっております。<br><br>
					※2018年9月15日現在、当局の組織変更に伴い、発給が止まっているビザがございます。<br>
					随時費用だけでなく取得できるビザカテゴリーに変更が行われる可能性が出ております。<br>
					毎日最新情報取得に努めております。<br>
					お気軽にお問い合わせ下さい。
				</div><br>
				<span style="font-weight: bold; font-size: 18px;">
					ビザの発行場所について
				</span><br>
				<div>
					ビザの発行機関は、現在各国のベトナム大使館と領事館、ベトナムの国際空港、国境となっております。<br>
					※ホーチミン市のタンソンニャット空港など国際空港に到着時発行してもらうビザをアライバルビザと言います。<br>
					発行機関に、パスポート、申請書、ビザ公電（ビザ取得証明書）を提出すると、パスポートにビザを貼ってくれます。<br>
					発行機関では、ビジネスビザ、観光ビザ、駐在員事務所ビザ、家族帯同ビザの発行が可能です。<br>
					弊社では、空港や大使館でビザを発行されるビザ公電（ビザの取得証明書）の取得を行っております。<br>
					日本のベトナム大使館、領事館受け取り、ベトナムの各国際空港受け取り、ベトナム国境受け取りだけでなく、各国の大使館受け取りの公電取得が可能です。<br>
					お気軽にご相談ください。<br>
					※現在、ベトナム国内取得できるビザは、ビジネスビザ更新、VRビザの更新、レジデンスカードの発行、更新のみとなっております。（観光ビザは最大88日までとなっており、1ヶ月ビザを88日ビザまで伸ばすのは可能です）
				</div><br>
