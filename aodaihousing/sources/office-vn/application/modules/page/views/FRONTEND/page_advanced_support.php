            <span style="font-weight: bold;">Step6 企業ライセンスの取得(ERC)</span>
        </div>
        <p style="line-height: 25px;">この後の印鑑作成作業が終わりましたら、投資ライセンスと企業ライセンスをお引渡し致します。
        </p><div class="table-responsive">
            <span style="font-weight: bold;"></span>
        </div>
        <div class="table-responsive">
            <span style="font-weight: bold;">Step7 印鑑の作成、登録</span>
        </div>
        <div class="table-responsive">
            <p style="line-height: 25px;">どの書類にも署名とここで制作する印鑑が必要になってきます。 </p>
            <span style="font-weight: bold;">Step8 銀行口座の開設</span>
            <p style="line-height: 25px;"> 資本金口座と経常口座を作成します。<br>
                作成の後まずは資本金口座に資本金を振り込みます。<br>
                その後経常口座に資金を移動させます。
            </p>
            <span style="font-weight: bold;">Step9 事業ライセンス税の支払い</span><br>
            税務ソフトのトークンを契約して頂き、そのソフトから初回事業ライセンス税を登録して税務署に税金を支払います。<br>
            <span style="font-weight: bold;">Step10 スタッフへの引き継ぎ</span><br>
            <p style="line-height: 25px;">入社されたスタッフの方に引き継ぎます。</p>
            <p style="line-height: 25px;">ここからは各初期設定を行うことになります。</p>
            <p style="line-height: 25px;">状況に応じて弊社のサポートも可能です。</p>
            <p style="line-height: 25px;">この後に営業開始する為のライセンス取得に動きます。</p>
