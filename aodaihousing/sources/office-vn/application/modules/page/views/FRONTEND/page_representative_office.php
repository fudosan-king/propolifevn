                <span style="font-size: 18px; font-weight: bold;line-height: 35px">駐在員事務所設立の流れ</span>
            </p>
            <p style="line-height: 25px;">
                <span style="font-weight: bold;">Step1 進出相談会</span>
            </p>
            <p style="line-height: 25px;">進出相談会では、まずはどのような事業を行いたいのか、いつ頃から開始したいのかお伺いさせてください。</p>
            <p style="line-height: 25px;">最適な進め方をご提案させて頂きます。<br>
                <span style="font-weight: bold;">Step2 オフィスの契約</span>
            </p>
            <p style="line-height: 25px;">進出手続きにはオフィスの契約が必要になります。設立申請書に設立会社の住所を記載しなければならなく、また提出書類としてオフィスとの賃貸契約書の公証が必要になります。<br>
                <span style="font-weight: bold;">Step3 申請添付書類の取得と認証作業、書類のベトナム語翻訳、申請書の作成</span>
            </p>
            <p style="line-height: 25px;">申請時に必要な書類を本社所在国で取得していただきます。親会社が日本の場合、日本の外務省認証を行って頂きます。その後、ベトナム外務省での認証を行いベトナムで利用できるようになります。この書類は設立申請書を作成にも利用致します。翻訳、書類作成で約3週間程度かかります。<br>
                <span style="font-weight: bold;">Step4 設立申請</span>
            </p>
            <p style="line-height: 25px;">作成した申請書類に親会社代表者、現地代表者になられる方のサインと本社の社判を押して頂き申請致します。</p>
            <p style="line-height: 25px;">※事務所運営上、設立申請期間中に行なうべき準備もサポートしております。
            <br>
