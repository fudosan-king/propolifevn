        </div><br>
        <div>
            <div>
                労働許可証は、ベトナムにおいて外国人が働くために必要になります。<br>
                弊社では、毎月多く申請をしていることから、変更が多い当局の対応の最新情報でサポートをしております。<br>
                労働許可証の取得、免除申請、更新、レジデンスカード、家族付帯ビザもサポート実績豊富なロータスサービスにお任せ下さい。<br>
                労働許可証の申請は、働かれる会社のライセンス内容とお持ちの経験が大切になります。<br>
                幣サービスでは、経験豊富な日本人担当者とベトナム人弁護士で申請前の書類取得段階、書類作成段階からサポートしております。<br>
                労働許可証もしくは免除を取得しますとレジデンスカード（短期滞在許可証/2年のマルチビザ）の取得もできます。<br>
                今のところ管理者、技術者、専門家の3つのカテゴリーについて変更はありません。また取得手続きではなく、免除申請手続きになる方もいらっしゃいます。※1<br>
                当局への提出書類の内容に1部変更が発生しており、経験についてのヒアリング内容に変更が出ております。<br>
                当局の担当者が、個別に要望する必要書類が変わることを防ぐため、管理者、技術者、専門家に合わせた専門家証明書などの書類作成も重要になってきます。<br>
                引き続き取得希望時期の2ヶ月前くらいにご相談下さい。<br>
                提出時の正確な情報を基にご提案をさせて頂いております。<br>
                大学卒業でない場合や取得、申請が失敗されて再申請をご希望の場合もお気軽にご相談下さい。<br>
                ※1労働許可証免除申請の場合必要書類が軽減されます。<br>
                労働許可証免除手続き、取得どちらが必要になるかは、会社のライセンス書類を確認させて頂いております。<br>
                ※取得中のビザもご提案させて頂いております。詳しくは<a href="https://www.propolifevietnam.com/support/%E3%83%99%E3%83%88%E3%83%8A%E3%83%A0%E3%83%93%E3%82%B6/" target="_blank" style="text-decoration: none;color: blue">ビザページ</a>をご覧ください。
